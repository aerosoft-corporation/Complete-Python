# Elif statements use for long solving problems
# Park Ticket Price #Example for understanding
age = int(input("Enter Age: "))
if age <= 0:
    print("You Cannot Enter")
elif 0<age<=3:
    print("Free")
elif 3<age<=10:
    print("Rs: 150")
elif 10<age<=30:
    print("Rs: 200")
elif 30<age<=60:
    print("Rs: 250")
else:
    print('Rs: 300')



