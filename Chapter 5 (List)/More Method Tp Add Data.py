# Some Method To Add Data In Our List
# 1.Insert Method 
# 2.How To Join Two List (Concatination)
# 3.Extend Method


# 1. Insert Method  --->   We Can Give Postion To Our Element
name = ['Abbas', 'Mubashar', 'Abdulwahab']
name.insert(1,'Bilal')
print(name)

# 2.Concatinate Two List
Fruits = ['Apple', 'Orange']
Fruit2 = ['Mango', 'Banana']
fruits = Fruits + Fruit2
print(fruits)

# 3. Extend Method
material = ['Flower', 'Sunny']
material2 = ['Travera', 'Granite']
material.extend(material2)
print(material)